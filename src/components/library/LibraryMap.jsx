import { useMemo, useState } from 'react'
import { ComposableMap, Geographies, Geography, Marker } from 'react-simple-maps'
import { computeSubScores, safeScore } from '../../lib/scoreEngine'
import countyCentroids from '../../data/county_centroids.json'

// Phase 2B · TRACTOVA-UX-001 — Library Map view.
//
// US choropleth + project pins for the user's saved pipeline. Distinct
// visual identity from Dashboard's USMap so users can tell the two apart
// at a glance:
//   - Dashboard USMap colors states by the *market* feasibility score
//     across all states. State coloring reads as the market opportunity.
//   - LibraryMap colors states by the *portfolio* — MW-weighted average
//     of the user's saved-project live scores per state. States without
//     any saved project are muted gray. State coloring reads as "what
//     does your book look like in this state?"
//
// Pins are county-centroid placed via src/data/county_centroids.json
// (generated by scripts/generate-county-centroids.mjs). Each pin colored
// by its individual project's live score, sized by MW. Click a pin to
// open ProjectDrawer with full project detail.
//
// Above 200 projects we cluster by state (sqrt-scaled bubble with
// project count) — keeps the SVG node budget bounded and the map
// readable on screenshots.

const GEO_URL = 'https://cdn.jsdelivr.net/npm/us-atlas@3/states-10m.json'

// FIPS code → USPS abbreviation. Same map as USMap.jsx — kept in lockstep
// so saved-project state codes resolve consistently across both surfaces.
const FIPS = {
  '01':'AL','02':'AK','04':'AZ','05':'AR','06':'CA','08':'CO','09':'CT',
  '10':'DE','11':'DC','12':'FL','13':'GA','15':'HI','16':'ID','17':'IL',
  '18':'IN','19':'IA','20':'KS','21':'KY','22':'LA','23':'ME','24':'MD',
  '25':'MA','26':'MI','27':'MN','28':'MS','29':'MO','30':'MT','31':'NE',
  '32':'NV','33':'NH','34':'NJ','35':'NM','36':'NY','37':'NC','38':'ND',
  '39':'OH','40':'OK','41':'OR','42':'PA','44':'RI','45':'SC','46':'SD',
  '47':'TN','48':'TX','49':'UT','50':'VT','51':'VA','53':'WA','54':'WV',
  '55':'WI','56':'WY',
}

const CLUSTER_THRESHOLD = 200  // above this, swap individual pins for per-state cluster bubbles

function colorForScore(score) {
  if (score == null)  return '#9CA3AF'
  if (score >= 70)    return '#0F766E'  // teal
  if (score >= 50)    return '#D97706'  // amber
  return '#DC2626'                       // red
}

// Translate a portfolio-aggregate score → state fill on the map.
// Distinct from the Dashboard USMap's market-feasibility ramp so users
// don't read "blank state" as "weak market" — it's "no saved projects".
function stateFillForAggregate(agg) {
  if (!agg) return '#F1F5F9'  // slate-100 — no projects in this state
  const score = agg.weightedAvg
  if (score == null)  return '#E2E8F0'
  if (score >= 70)    return '#0F766E'  // teal-700 — strong portfolio
  if (score >= 60)    return '#14B8A6'  // teal-500
  if (score >= 50)    return '#5EEAD4'  // teal-300 — moderate
  if (score >= 40)    return '#FCD34D'  // amber-300 — warning
  return '#FCA5A5'                       // red-300 — weak portfolio
}

export default function LibraryMap({
  projects,
  stateProgramMap,
  countyDataMap,
  onStateClick,
  onPinClick,
  filterState,
}) {
  const [hoveredStateId, setHoveredStateId] = useState(null)
  const [hoveredPinId,   setHoveredPinId]   = useState(null)
  const [tooltip,        setTooltip]        = useState(null)

  // Per-state portfolio aggregates — MW-weighted average of live scores.
  // Computed once per projects/state-map change. Each entry:
  //   { mwSum, scoreSum, weighted, weightedAvg, count }
  const stateAggregates = useMemo(() => {
    const agg = {}
    for (const p of projects) {
      if (!p.state) continue
      const sp = stateProgramMap[p.state]
      if (!sp) continue
      const cd = countyDataMap?.[`${p.state}::${p.county}`] || null
      const subs = computeSubScores(sp, cd, p.stage, p.technology)
      const score = safeScore(subs.offtake, subs.ix, subs.site)
      if (score == null) continue
      const mw = parseFloat(p.mw) || 0
      if (!agg[p.state]) agg[p.state] = { mwSum: 0, weighted: 0, count: 0, totalScore: 0 }
      // If MW is zero/missing, still count the project but with weight 1
      // so it isn't silently dropped from the aggregate.
      const w = mw > 0 ? mw : 1
      agg[p.state].mwSum    += w
      agg[p.state].weighted += w * score
      agg[p.state].totalScore += score
      agg[p.state].count    += 1
    }
    for (const k of Object.keys(agg)) {
      agg[k].weightedAvg = Math.round(agg[k].weighted / agg[k].mwSum)
      agg[k].avgScore    = Math.round(agg[k].totalScore / agg[k].count)
    }
    return agg
  }, [projects, stateProgramMap, countyDataMap])

  // Resolve each project to a centroid + live score for pin rendering.
  // Skip silently when we lack a state map row or a centroid lookup —
  // a missing pin is better than a pin at [0, 0] off the coast of Africa.
  const pins = useMemo(() => {
    return projects.map(p => {
      const sp = stateProgramMap[p.state]
      if (!sp || !p.state || !p.county) return null
      const centroid = countyCentroids[`${p.state}::${p.county}`]
      if (!centroid) return null
      const cd = countyDataMap?.[`${p.state}::${p.county}`] || null
      const subs = computeSubScores(sp, cd, p.stage, p.technology)
      const score = safeScore(subs.offtake, subs.ix, subs.site)
      const mw = parseFloat(p.mw) || 0
      return { id: p.id, name: p.name, state: p.state, county: p.county, mw, score, centroid, project: p }
    }).filter(Boolean)
  }, [projects, stateProgramMap, countyDataMap])

  const shouldCluster = pins.length > CLUSTER_THRESHOLD

  // When clustering, collapse pins to one bubble per state at the
  // approximate state centroid. Bubble size scales with sqrt(count) so
  // a 25-project state isn't 25× the visual weight of a 1-project state.
  const clusters = useMemo(() => {
    if (!shouldCluster) return []
    const byState = {}
    for (const pin of pins) {
      if (!byState[pin.state]) byState[pin.state] = { state: pin.state, pins: [], mwSum: 0 }
      byState[pin.state].pins.push(pin)
      byState[pin.state].mwSum += pin.mw
    }
    // Cluster anchor = mean of the state's pin centroids.
    return Object.values(byState).map(c => {
      const lon = c.pins.reduce((s, p) => s + p.centroid[0], 0) / c.pins.length
      const lat = c.pins.reduce((s, p) => s + p.centroid[1], 0) / c.pins.length
      return { state: c.state, count: c.pins.length, mwSum: c.mwSum, centroid: [lon, lat] }
    })
  }, [pins, shouldCluster])

  return (
    <div
      className="rounded-xl overflow-hidden relative"
      style={{
        background: 'linear-gradient(160deg, #FFFFFF 0%, #F0FDFA 100%)',
        border: '1px solid rgba(20,184,166,0.18)',
        boxShadow: '0 0 0 1px rgba(20,184,166,0.06), 0 8px 32px rgba(0,0,0,0.08), 0 2px 8px rgba(0,0,0,0.05)',
      }}
    >
      <div
        className="relative z-10 px-5 pt-3 pb-2"
        style={{ borderBottom: '1px solid rgba(20,184,166,0.10)', background: 'rgba(20,184,166,0.04)' }}
      >
        <div className="flex items-baseline justify-between gap-3 flex-wrap">
          <div>
            <span className="eyebrow-mono" style={{ color: '#5A6B7A' }}>Portfolio Geography</span>
            <h2 className="text-sm font-semibold text-gray-800 mt-0.5">
              {projects.length === 0
                ? 'Your pipeline on the map'
                : `${projects.length} project${projects.length === 1 ? '' : 's'} across ${Object.keys(stateAggregates).length} state${Object.keys(stateAggregates).length === 1 ? '' : 's'}`
              }
            </h2>
          </div>
          <p className="text-[11px] text-gray-400">
            {shouldCluster ? 'Clustered by state (200+ pins)' : 'Click a pin for project detail · click a state to filter'}
          </p>
        </div>
      </div>

      <div className="relative z-10">
        <ComposableMap
          projection="geoAlbersUsa"
          projectionConfig={{ scale: 1000 }}
          style={{ width: '100%', height: 'auto' }}
        >
          <Geographies geography={GEO_URL}>
            {({ geographies }) => geographies.map((geo) => {
              const fips = String(geo.id).padStart(2, '0')
              const stateId = FIPS[fips]
              if (!stateId) return null
              const agg = stateAggregates[stateId]
              const hasProjects = !!agg
              const isHovered  = hoveredStateId === stateId
              const isFiltered = filterState === stateId
              const baseFill = stateFillForAggregate(agg)
              // Hover deepens the fill; active filter gets a violet halo
              // (matches the Dashboard USMap's selection color so the
              // visual grammar is consistent across surfaces).
              const fill = isFiltered ? '#7C3AED' : isHovered && hasProjects ? '#0F766E' : baseFill
              const aria = hasProjects
                ? `${stateId}: ${agg.count} project${agg.count === 1 ? '' : 's'}, ${agg.mwSum.toFixed(1)} MW, avg score ${agg.weightedAvg}. Press Enter to filter Library to this state.`
                : `${stateId}: no saved projects.`
              return (
                <Geography
                  key={geo.rsmKey}
                  geography={geo}
                  fill={fill}
                  stroke="#FFFFFF"
                  strokeWidth={0.5}
                  style={{ default: { outline: 'none' }, hover: { outline: 'none' }, pressed: { outline: 'none' } }}
                  onMouseMove={(evt) => {
                    setHoveredStateId(stateId)
                    setTooltip({ x: evt.clientX, y: evt.clientY, kind: 'state', stateId, agg })
                  }}
                  onMouseLeave={() => { setHoveredStateId(null); setTooltip(null) }}
                  onClick={() => onStateClick?.(stateId, hasProjects)}
                  onKeyDown={(evt) => { if (evt.key === 'Enter' || evt.key === ' ') { evt.preventDefault(); onStateClick?.(stateId, hasProjects) } }}
                  className={`transition-all duration-100 focus:outline-hidden focus-visible:ring-2 focus-visible:ring-teal-500 ${hasProjects ? 'cursor-pointer' : 'cursor-default'}`}
                  role="button"
                  tabIndex={hasProjects ? 0 : -1}
                  aria-label={aria}
                  aria-pressed={isFiltered}
                />
              )
            })}
          </Geographies>

          {/* Markers layer — individual pins OR per-state clusters above
              the cluster threshold. Drawn AFTER Geographies so they paint
              on top. */}
          {!shouldCluster && pins.map((pin) => {
            const color = colorForScore(pin.score)
            const isHovered = hoveredPinId === pin.id
            // Pin radius scales with sqrt(mw) so a 50 MW project isn't 50×
            // the visual weight of a 1 MW one, but the size still encodes
            // capacity. Clamped to a sensible 3.5–7.5 px range.
            const r = Math.max(3.5, Math.min(7.5, 3.5 + Math.sqrt(Math.max(0, pin.mw)) * 0.6))
            return (
              <Marker key={pin.id} coordinates={pin.centroid}>
                <circle
                  r={isHovered ? r + 1.2 : r}
                  fill={color}
                  fillOpacity={0.85}
                  stroke="#FFFFFF"
                  strokeWidth={1.2}
                  style={{ cursor: 'pointer', transition: 'r 140ms ease-out' }}
                  onMouseEnter={(evt) => {
                    setHoveredPinId(pin.id)
                    setTooltip({ x: evt.clientX, y: evt.clientY, kind: 'pin', pin })
                  }}
                  onMouseMove={(evt) => setTooltip(t => t ? { ...t, x: evt.clientX, y: evt.clientY } : t)}
                  onMouseLeave={() => { setHoveredPinId(null); setTooltip(null) }}
                  onClick={(evt) => { evt.stopPropagation(); onPinClick?.(pin.project) }}
                  role="button"
                  aria-label={`${pin.name} — ${pin.mw} MW, score ${pin.score ?? 'unknown'}. Click to view details.`}
                />
              </Marker>
            )
          })}

          {shouldCluster && clusters.map((c) => {
            const r = Math.max(8, Math.min(28, 8 + Math.sqrt(c.count) * 3))
            return (
              <Marker key={c.state} coordinates={c.centroid}>
                <circle
                  r={r}
                  fill="#0F766E"
                  fillOpacity={0.32}
                  stroke="#0F766E"
                  strokeWidth={1.5}
                  style={{ cursor: 'pointer' }}
                  onClick={() => onStateClick?.(c.state, true)}
                  aria-label={`${c.state} — ${c.count} projects, ${c.mwSum.toFixed(1)} MW. Click to filter to this state.`}
                />
                <text
                  textAnchor="middle"
                  y={4}
                  className="font-mono font-bold tabular-nums pointer-events-none"
                  style={{ fontSize: '11px', fill: '#0F1A2E' }}
                >{c.count}</text>
              </Marker>
            )
          })}
        </ComposableMap>
      </div>

      {/* Hover tooltip — kept dark-on-light to match the Dashboard USMap's
          tooltip vocabulary. Positioned against the viewport so it can
          escape the map's overflow-hidden container. */}
      {tooltip && tooltip.kind === 'state' && (
        <div className="fixed z-50 pointer-events-none" style={{ left: tooltip.x + 12, top: tooltip.y - 10 }}>
          <div
            className="text-xs rounded-lg px-3 py-2 shadow-2xl max-w-[220px]"
            style={{ background: 'rgba(7,17,12,0.95)', border: '1px solid rgba(20,184,166,0.30)' }}
          >
            <div className="font-semibold" style={{ color: 'rgba(255,255,255,0.92)' }}>{tooltip.stateId}</div>
            {tooltip.agg ? (
              <div className="mt-1 flex flex-col gap-0.5 text-[11px]" style={{ color: 'rgba(255,255,255,0.7)' }}>
                <span className="font-mono tabular-nums">
                  <span className="text-white font-semibold">{tooltip.agg.count}</span> project{tooltip.agg.count === 1 ? '' : 's'} ·
                  <span className="text-white font-semibold ml-1">{tooltip.agg.mwSum.toFixed(1)} MW</span>
                </span>
                <span className="font-mono tabular-nums">
                  MW-weighted avg score: <span className="text-white font-semibold">{tooltip.agg.weightedAvg}</span>
                </span>
                <span className="text-[10px] mt-0.5" style={{ color: 'rgba(255,255,255,0.4)' }}>Click to filter Library →</span>
              </div>
            ) : (
              <div className="mt-0.5 text-[11px]" style={{ color: 'rgba(255,255,255,0.4)' }}>No saved projects</div>
            )}
          </div>
        </div>
      )}
      {tooltip && tooltip.kind === 'pin' && (
        <div className="fixed z-50 pointer-events-none" style={{ left: tooltip.x + 12, top: tooltip.y - 10 }}>
          <div
            className="text-xs rounded-lg px-3 py-2 shadow-2xl max-w-[260px]"
            style={{ background: 'rgba(7,17,12,0.95)', border: '1px solid rgba(20,184,166,0.30)' }}
          >
            <div className="font-semibold" style={{ color: 'rgba(255,255,255,0.92)' }}>{tooltip.pin.name}</div>
            <div className="mt-0.5 text-[11px] font-mono tabular-nums" style={{ color: 'rgba(255,255,255,0.55)' }}>
              {tooltip.pin.county}, {tooltip.pin.state} · {tooltip.pin.mw} MW
            </div>
            <div className="mt-1 text-[11px] font-mono tabular-nums" style={{ color: '#2DD4BF' }}>
              Score: <span className="font-bold">{tooltip.pin.score ?? '—'}</span>
            </div>
            <div className="mt-0.5 text-[10px]" style={{ color: 'rgba(255,255,255,0.4)' }}>Click for details →</div>
          </div>
        </div>
      )}
    </div>
  )
}
